class QuestionFree extends Question
{
    /**
     * @var string
     */
    private $answer;

    /**
     * Set answer
     *
     * @param string $answer
     * 
     * @return QuestionFree
     */
    public function setAnswer($answer)
    {
        $this->answer = $answer;

        return $this;
    }

    /**
     * Get answer
     *
     * @return string 
     */
    public function getAnswer()
    {
        return $this->answer;
    }
}
